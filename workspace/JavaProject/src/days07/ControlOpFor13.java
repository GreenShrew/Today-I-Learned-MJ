package days07;

public class ControlOpFor13 {

	public static void main(String[] args) {
		int i,j;
		for(i=1;i<=10;i++) {
		 	for(j=1;j<=10;j++) {
//		 		System.out.printf("#");		10x10의 # 사각형이 나온다.
		 		System.out.printf("(%d, %d)  ",i,j);		// 10x10의 (i, j)가 나온다.
		 	}
			System.out.println();
		}

	}

}
