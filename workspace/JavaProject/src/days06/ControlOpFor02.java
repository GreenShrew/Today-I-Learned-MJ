package days06;

public class ControlOpFor02 {

	public static void main(String[] args) {
		int i;
		// 1부터 100 사이의 정수 출력
		System.out.println("정수 출력 --------------------------------");
		for(i=1;i<=100;i++) {
			System.out.printf("%d ",i);
		}
		System.out.printf("\n반복 실행후의 i 변수값 : %d\n",i);
		
		System.out.println("\n 1부터 100사이의 짝수 출력 #1----------------------");
		for(i=1;i<=100;i++) {
			if(i%2==0) {
				System.out.printf("%d ",i);
			}
		}
		System.out.printf("\n반복 실행후의 i변수 값 : %d\n",i);
		System.out.println("\n 1부터 100사이의 짝수 출력 #2----------------------");
		for(i=2;i<=100;i+=2) {
			System.out.printf("%d ",i);
		}
		System.out.printf("\n반복 실행후의 i변수 값 : %d\n",i);	
		// #1의 코드의 for문의 조건을 바꿔주는 것으로 #2와 같이 중괄호 { } 안에 들어갈 if문을 뺄 수 있었다.
		// 다만 #1, #2 실행이 끝난 후 i의 값이 서로 101, 102로 다름을 알 수 있다.
	}

}
